	/**
	 * Para un caso, genera el estado, los hijos, y comprueba si los
	 * hijos generados se corresponden con los esperados.
	 * 
	 * Cada caso contiene dos elementos:
	 * - los datos de un estado
	 * - un array con los datos de los hijos esperables
	 * 
	 */
	public boolean probarCaso(Object[][][][] caso){
		
		Object[][] datosEstado = caso[0][0];
		Object[][][] datosHijos = caso[1];
		
		//Obtenemos los datos del estado a crear
		int[] fichasJ1 = copiar(datosEstado[0]);
		int[] fichasJ2 = copiar(datosEstado[1]); 
		int[] golpeadas = copiar(datosEstado[2]);
		int[] liberadas = copiar(datosEstado[3]);
		boolean turno1 = (Boolean)datosEstado[4][0];
		int tiradaDado = (Integer) datosEstado[5][0];		
		
		
		MiniGammon e = new MiniGammon(fichasJ1, fichasJ2, golpeadas, liberadas, turno1, tiradaDado);
		ArrayList<MiniGammon> hijos = (ArrayList<MiniGammon>) e.hijos();
		
		if (hijos.size() != datosHijos.length){
			System.out.println(" No coincide el n�mero de hijos. Hay " + hijos.size() + " en lugar de " + datosHijos.length);
			return false; //el número de hijos no coincide
		}
		
		//comprobamos los hijos uno a uno
		for(int i = 0; i < hijos.size(); i++){
			if (!(esHijo(hijos.get(i), datosHijos))){
				System.out.println("  - El hijo " + i + " no se corresponde con los datos");
				hijos.get(i).ver();
				return false; //hay un hijo que no está en la lista de los esperados
			}//if !
		}
		
		//si las longitudes coinciden y están todos, entonces todo fue bien
		System.out.println("  OK");
		return true;
	}
	
	
	/**
	 * @return True si el estado e coincide con alguno de los hijos de la lista de datosHijos
	 */
	private boolean esHijo (MiniGammon e, Object[][][] datosHijos){
		
		for(int i = 0; i < datosHijos.length; i ++){
			if (corresponde(e, datosHijos[i])){
				return true;
			}// if corresponde
		}//for i
		
		return false;
	}
	
